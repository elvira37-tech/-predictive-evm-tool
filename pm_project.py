import matplotlib.pyplot as plt

def draw_ann_paper_style(
    layer_sizes,
    input_labels=None,
    output_labels=None,
    hidden_labels=None,
    title=None,
    figsize=(10, 12),
    node_size=20,
    line_width=0.35,
    line_grey="0.80"
):

    n_layers = len(layer_sizes)
    max_nodes = max(layer_sizes)

    fig, ax = plt.subplots(figsize=figsize)
    ax.axis("off")

    node_positions = []

    for li, n_nodes in enumerate(layer_sizes):
        slots = [1 - i / (max_nodes - 1) for i in range(max_nodes)] if max_nodes > 1 else [0.5]

        if n_nodes == 1:
            ys = [0.5]
        else:
            start = (max_nodes - n_nodes) // 2
            ys = slots[start:start + n_nodes]

        xs = [li] * n_nodes
        node_positions.append(list(zip(xs, ys)))

    for li in range(n_layers - 1):
        for (x1, y1) in node_positions[li]:
            for (x2, y2) in node_positions[li + 1]:
                ax.plot([x1, x2], [y1, y2], color=line_grey, linewidth=line_width, zorder=1)

    for li in range(n_layers):
        xs = [p[0] for p in node_positions[li]]
        ys = [p[1] for p in node_positions[li]]
        ax.scatter(xs, ys, s=node_size, color="black", zorder=2)

    if input_labels:
        for i, lbl in enumerate(input_labels):
            ax.text(-0.25, node_positions[0][i][1], lbl, ha="right", va="center", fontsize=11)
        ax.text(-0.25, 1.04, f"Input ({layer_sizes[0]})", ha="right", va="bottom", fontsize=11, style="italic")

    if output_labels:
        for i, lbl in enumerate(output_labels):
            ax.text(n_layers - 1 + 0.18, node_positions[-1][i][1], lbl, ha="left", va="center", fontsize=11)
        ax.text(n_layers - 1 + 0.18, 1.04, f"Output ({layer_sizes[-1]})", ha="left", va="bottom", fontsize=11, style="italic")

    if hidden_labels:
        hidden_layer_indices = list(range(1, n_layers - 1))
        for txt, li in zip(hidden_labels, hidden_layer_indices):
            ax.text(li, 1.04, txt, ha="center", va="bottom", fontsize=11, style="italic")

    if title:
        ax.set_title(title, fontsize=13, pad=16)

    ax.set_xlim(-0.6, n_layers - 0.4)
    ax.set_ylim(-0.05, 1.10)

    plt.show()



