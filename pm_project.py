def get_forecast():
    if assets is None:
        return np.ones(12)
    proj_vec = [0] * len(assets['encoded_project_cols'])
    try:
        proj_idx = assets['encoded_project_cols'].index(f"Type_{building_type.value}")
        proj_vec[proj_idx] = 1
    except:
        pass

    avg_cost_global = assets.get('avg_cost_weights_global', assets.get('avg_cost_weights', [1/6]*6))
    avg_time_global = assets.get('avg_time_weights', [1/6]*6)

    c_weights = assets.get('type_cost_weights', {}).get(building_type.value, avg_cost_global)
    t_weights = assets.get('type_time_weights', {}).get(building_type.value, avg_time_global)

    split_costs = [total_cost.value * c_weights[i] for i in range(6)]
    split_times = [total_time.value * t_weights[i] for i in range(6)]

    overrun_adj = assets.get('global_overrun_avg', 1.0)
    scaled_costs = [total_cost.value * avg_cost_global[i] * overrun_adj for i in range(6)]

    core_cont = [area_m2.value, num_floors.value, floor_height.value, total_cost.value, total_time.value]

    if len(assets['param_cols']) == 15:
        full_input = proj_vec + core_cont + scaled_costs
    else:
        full_input = proj_vec + core_cont + split_costs + split_times + scaled_costs

    feat = (np.array(full_input) - assets['params_min']) / assets['params_range']
    pred = model(jnp.array([feat]))[0]
    return np.array(pred) * assets['output_range'] + assets['output_min']

